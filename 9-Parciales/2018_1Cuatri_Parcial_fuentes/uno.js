
function mostrar()
{
    var ancho;
    var largo;
    var perimetro;
    
    
    ancho = parseInt(prompt("ingrese el ancho del rectangulo"));
    
    largo = parseInt(prompt("ingrese el largo del rectangulo"));
    
    perimetro = (ancho + largo)* 2;
    
    alert("El perimetro del triangulo es :" + perimetro);
    

}
